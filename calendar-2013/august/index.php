												<span style="font-size:40px;font-weight:bold;">HORSE the Band</span><br>
												Калифорнийская команда HORSE the Band, безусловные лидеры направления nintendocore.
												Группа ведет свою историю с 1999 года, которым датирована их первая демозапись
												«Scabies, The Kangarooster, and You», но впервые заявляет о себе летом 2002 года,
												организовав собственное турне по 7 странам. Первый серьезный альбом выходит на лейбле
												Pluto Records в 2003 году, под названием «R. Borlax». Благодаря соединению панка,
												металкора, истошного пронизывающего вокала Nathan Winneke и имитации музыки из
												приставочных игр получается взрывоопасная смесь, которая мало кого из слушателей
												может оставить равнодушным, в особенности на живых выступлениях! После выхода «R. Borlax»группа ведет взаимовыгодное сотрудничество с продюсером Мэттом Бэйлсом (Isis, Norma Jean) и на лейбле Combat Records в 2005 выпускает второй альбом «The Mechanical Hand». В 2006 году группа выступала со множеством разных музыкантов. Они ездили по USA с такими группами, как The Fall of Troy, Poison the Well, и Criteria. Через месяц после завершения тура, 28 мая 2006, HORSE начали тур с Gatsby’s American Dream, Portugal. The Man, и Forgive Durden.<br><br>
												Но группа рано вышла из этого тура, объяснив это тем, что члены группы не могли продолжать выступать, так как они были в Чикаго и ели пиццу, они уверяли, что эта пицца - лучшая пицца, которую они когда-либо ели. После этого группа участвовала еще в нескольких турах, и, в течение этих туров, они записали Pizza EP и играли вместе с GWAR. 2007 г. - появился третий полноформатник «Natural Death».<br><br>
												В сентябре 2009 вышел очередной альбом бэнда Desperate Living.<br><br>
