								<div id="desc_v_desc">
									<span style="font-size:40px;font-weight:bold;">Armin van Buuren</span><br>
									Голландия (1995 – по сей день)
									Армин ван Бюрен — один из самых известных диджеев, имеющий ярковыраженную прогрессив-трансовую
									направленность в творчестве. Армин был признан лучшим диджеем 2007, 2008, 2009, 2010, 2012 годов
									согласно опросу журнала DJ Mag. Единственный диджей, пять раз, из них 4 раза подряд удостоенный
									такого звания. По данным журнала Forbes, в 2013 году он входил в десятку самых высокооплачиваемых
									диджеев мира с годовым доходом в 17 миллионов долларов.<br><br>
									Появившись в топе 100 самых популярных диджеев ещё в 2001 году, он последние 7 лет неизменно входил в
									пятерку лучших, и наконец ему удалось подняться со второго места на вершину топа. <br><br>
									Первый релиз Армин выпустил в 18 лет, впервые попал в чарт Великобритании со своим «Blue Fear» в 20 лет,
									и образовал свой лейбл в 22 года. В 23 года он опять попал в чарт Великобритании с «Communication».
									Сейчас он выпускает сборник своих дисков, и, будучи продюсером и диджеем одновременно, он медленно но верно завоевывает мир.<br><br>
									Армин родился 25 декабря 1976 года в Лейдене, Голландия. Сколько он себя помнит, он был всегда очарован музыкой.
									В основном благодаря своему отцу, который слушал музыку множества стилей. Кроме своей любви к музыке Армин постепенно
									начал интересоваться технологиями и компьютерами. В 14 лет он купил свой первый семплер Akai S-01, «конфисковал» отцовский 
									синтезатор и начал создавать музыку. В юношестве он ходил в школу почти всегда надев свои наушники, слушая разнообразную музыку, 
									от Жана Мишеля Жара (Jean-Michael Jarre) до минимиксов Бена Либранда (Ben Liebrand). Очень уважая Бена, он послал ему свою демо-запись
									и получил восторженный отзыв. Самая первая демо-композиция была сразу же включена в сборник. На деньги, которые он получил от этой сделки,
									Армин купил гораздо лучшее оборудование, и это отразилось на его первом виниловом диске, выпущенном в конце 1995 года. Также Бен обучил Армина
									продюсированию и микшированию. Его страсть к микшированию усилилась.<br><br>
									Именно там он начал свою карьеру в качестве диджея в местном клубе Nexus, где научился играть длинные 6—7-часовые сеты. В 1995 году он поступил
									в университет изучать право, а после лекций играл в различных клубах и сочинял собственную музыку. В это же время он переносит студию из своей 
									спальни в реальную студию. И все первые треки вышли из этой студии, включая такие хиты как «Touch Me» и «Communication».<br><br>

									С 1995 года Армин выпустил много треков на разных лейблах со всё возрастающим успехом. Первым большим успехом был «Blue Fear»,
									выпущенный на Cyber Records. Трек «Euro Trance Blueprint» даже попал в чарт Великобритании. На этом же лейбле он выпустил «Communication»,
									который произвел фурор на Ибице летом 1999 года. Этот трек был подписан к AM:PM и вошёл в чарт Великобритании под 18 номером в 2000 году!<br><br>

									В начале 1999-го года он основал свой лейбл Armind с United Recordings. Первый релиз «One» под именем Gig был очень хорошо принят. 
									Он выпустил «4 Elements» под именем Gaia и объединился с DJ Tiesto, чтобы создать 2 проекта: Major League «Wonder Where You Are?»
									на Blackhole и Alibi «Eternity», которые были также выпущены на Armind. «Eternity» был лицензирован на лейбле Пола ван Дайка(Paul Van Dyk)
									VANDIT Records. Затем последовал ещё один совместный проект: он объединился с Ferry Corsten, чтобы записать «Exhale» для альбома «The System F». 
									Лейбл Armind постепенно превратился в огромную трансовую империю под названием Armada Music, с множеством подлейблов, вроде A State Of Trance,
									68 Recordings, Bandung, Captivating Sounds, Club Elite, Coldharbour Recordings, Electronic Elements и Fame Recordings. Эта империя выпускает огромное
									количество разнообразных трансовых (и не только) хитов, и считается на данный момент самой большой и успешной коалицией трансовых лейблов.<br><br>

									После ремиксинга популярнейших треков, таких как «King Of My Castle» для Wamdue Project, «Don’t Call Me Baby» для Madison Avenue и
									«Walhalla» для Gouryella, Армин стал высокозапрашиваемым ремиксером и он отвергает просьбы о ремиксах чуть ли не ежедневно. Армин не
									любит ограничивать себя определенным стилем музыки. Выражение, которое он часто использует: «Не будь пленником собственного стиля» 
									(Don’t be prisoner of your own style). Армин любит описывать свой музыкальный стиль как эйфорический, возвышающий, мелодический и 
									энергетический («euphoric, uplifting, melodic and energetic»). То, что Армин очень амбициозен, это факт, но он делает множество вещей 
									в одно и то же время: ведет студийную и продюсерскую работу, является диджеем. Он верит, что есть только один способ что-то сделать — 
									получать удовольствие от того, что делаешь. Армин хочет, чтобы осуществились его мечты, а именно «делать то, что он любит больше всего
									и быть счастливым, делая это» (doing what he loves most and have as much fun as possible doing it). В последнее время Армин работает над
									своей карьерой диджея, играя в среднем два сета в неделю. Как он говорит, «фанаты и публика — наиболее важные вещи для диджея» (The fans
									and the public are the most important thing for a DJ).<br><br>

									В самом начале своего музыкального пути Армин часто издавался под разными псевдонимами, вроде Darkstar, Perpetuous Dreamer, Rising Star,
									Gaia, Gimmick. Он также создал несколько треков со своими собратьями «трансголландцами» — Tiesto и Ferry Corsten. Вместе с Tiesto он 
									выпустил в 2000 году треки «Eternity» под псевдонимом Alibi и «Wonder Where You Are?» в качестве Major League. А вместе с Ferry Corsten
									он создал крупный трансовый хит «Exhale» для его проекта System F. Однако с 2003 года он обычно выпускается под своим собственным именем,
									хотя часто использует псевдонимы для ремиксов чужих и своих треков.<br><br>

									В 1999 году Армин выпустил свою первую компиляцию «Boundaries of Imagination», за которой в 2000 году последовала целая серия из четырех
									компиляций «A State of Trance», «Basic Instinct», «In Motion» и «Transparance», которые органично совмещали в себе любовь Армина к 
									прогрессив-музыке, техно и трансу. Другая серия его компиляций, запущенная в 2003 году «Universal Religion», в этом году увидела уже
									третью часть.<br><br>

									В марте 2001 года Армин запустил свое собственное радиошоу на радио ID&T под названием «A State of Trance». Каждую неделю в течение 
									двух часов он играет самые новые популярные трансовые и прогрессив-хиты, открывая дорогу к славе многим малоизвестным музыкантам. 
									Это радиошоу в мае 2007 года отметило свой юбилейный 300-й выпуск, и на данный момент является самым популярным трансовым шоу в мире.
									На основе данного шоу Армин создал одноименную компиляцию, ежегодно с 2004 года выпуская по паре дисков самых популярных треков, прозвучавших в ходе передачи.<br><br>

									В 2003 году Армин ван Бюрен выпустил свой дебютный альбом «76», содержавший такие мегахиты, как: «Yet Another Day», «Burned With Desire»,
									а также новые версии «Blue Fear» и «Communication». Следующий альбом «Shivers» вышел уже в 2005 году, и кроме одноименного хита, содержал
									также выпущенные в виде синглов треки «Serenity», «Zocalo», «Who is Watching», «Sail» и «Control Freak». В создании этих двух альбомов 
									активно участвовали и другие музыканты: Gabriel & Dresden, Airwave, System F и M.I.K.E., а также вокалисты Justine Suissa, Nadia Ali и Ray Wilson.<br><br>

									Третий альбом, вышедший в 2006 году под названием «10 Years», был составлен из трех дисков самых лучших работ Армина за последние десять лет,
									новых версий старых хитов, ремиксов, а также несколько совершенно новых треков: «Love You More», «Saturday Night» и «This World Is Watching Me».<br><br>

									Армин, пользуясь своей популярностью в качестве одного из самых лучших трансовых диджеев, активно путешествует по всему миру, выступая на
									самых крупных мероприятиях: Sensation White, Godskitchen и Gatecrasher. Те же, кому не удалось посмотреть его в живую, могут посмотреть записи
									его выступлений на DVD, существует два диска с записями его выступлений: «Armin Only — The Next Level» и «Ahoy».<br><br>

									Среди последних работ этого замечательного музыканта и диджея можно отметить сингл «Rush Hour» и новую версию его хита «The Sound Of Goodbye»,
									который был выпущен ещё в 2001 году под псевдонимом perpetuous dreamers.<br><br>

									В 2008 году Армин подтвердил свое звание диджея номер один в мире, во второй раз заняв первое место согласно опросу журнала DJ Mag.<br><br>

									Дата рождения: 25 декабря 1976 в городе Лейден, Нидерланды (Leiden, Netherlands);<br>
									Образование: Почти закончил изучать Закон в Лейденском Университете;<br>
									На него повлияли: LieBrand, Jean-Michel Jarre;<br>
									Любит: Кино;<br>
									Ненавидит: Людей, которые не делают то, что обещают;<br>
									Первый релиз: Armin — «Push»;<br>
									Лучшие ремиксы: Geoffrey Williams — «Sex Life», Wamdue Project — «King Of My Castle»;<br>
									Лучшие релизы: Perpetuous Dreamer — «The Sound of Goodbye»;<br>
									Собственный стиль: Не любит, когда его относят к какому-то стилю;<br>
									Любимый альбом: The Future Sound of London — «Lifeforms», Jean-Michel Jarre — «Oxygene»;<br>
									Любимые house-пластинки: L.S.G. — «Netherworld», Li Kwan — «Point Zero».<br><br>

									Проекты, в которых участвовал AvB:<br>
									— Alibi (совместно с DJ Tiёsto)<br>
									— Amsterdance<br>
									— Armania<br>
