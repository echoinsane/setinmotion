									<span style="font-size:40px;font-weight:bold;">P.O.D. +</span><br>
									<span style="font-size:25px;">Drowning Pool</span><br>
									Точной даты создания группы теперь никто и не назовет. В ранних 1990-х годах встретились два хороших друга — Маркос Кьюриел и Вав Бернардо для своей первой репетиции. Сначала их дуэт носил название «Eschatos». Они существовали без главного вокалиста около двух лет. У Вава был кузен Пол, его мать в 1992 году умерла от лейкемии. До этого Сонни, как его называли друзья, в 13 лет присоединился к банде района «Chicano» под Сан Диего, употреблял алкоголь и покуривал марихуану. Однако смерть 37-летней матери, которая была истинной христианкой, очень повлияла на Сонни, и обратила теперь уже 18-ти летнего юношу в эту религию. После случившегося он решил проповедовать христианские принципы посредством выступлений в музыкальной группе. Он присоединился к своему кузену и вместе с временным гитаристом Гейбом Портилло (Gabe Portillo) и Маркосом, начал выступать в группе «Enoch», которую с приходом Сонни, вскоре переименовали в знакомое теперь нам «P.O.D.» (аббревиатура «Payable On Death»). Стиль их музыки начал приобретать отчетливое звучание со смесью регги, рока и рэпа.<br><br>

									Траа Дениелз присоединился к группе в 1994 году, вытеснив из коллектива Гейба, чье присутствие в группе теперь можно услышать лишь на демо-записях ранних лет. «P.O.D.» подписывают контракт с тогда еще не известной звукозаписывающей компанией «Rescue Records», и выпускают на ней три альбома в период с 1994 по 1997 года — «Snuff the Punk», «Brown» и «Payable on Death Live». Последний альбом представляет собой DVD с концерта группы, записанный во время их выступления на фестивале «Tom Fest» в 1997 году. Это был третий, и последний альбом группы, записанный на «Rescue Records». Позднее, в 2001 году, лейбл переиздал эту запись в более полном варианте.<br><br>

									Вскоре после выпуска «Payable on Death Live», звукозаписывающий бренд «Essential Records» предложил ребятам из «P.O.D.» заключить контракт на сумму $100.000. Но Сонни попросил менеджера группы Тима Кука (Tim Cook) связаться с «Essential Records», и тактично отказаться от предложения, объяснив это тем, что «У Бога приготовлено более грандиозное будущее для P.O.D.».<br><br>

									В 1998 году Джон Рубели (John Rubeli) из «Atlantic Records» заметил выступления группы в клубах «The Roxy» и «Sunset Strip». Он предложил ребятам контракт. Группа сделала самый важный и успешный шаг, подписав его. «P.O.D.» вскоре выпустили альбом «The Warriors», посвященный своим преданным поклонникам и лидеру фан-клуба Джордану (Jordan Peter Douglas Orava). Это был этакий «транзитный диск», переводящий группу от эпохи сотрудничества с «Rescue Records» к «Atlantic Records». Было издано всего лишь 30 000 копий диска, поэтому он считается коллекционным, и труднодоступным для покупателей. Запись содержит обращение Сонни к своим фанатам, благодарности в адрес «Warriors» — так принято называть «клан поклонников» группы, которые все эти годы следили за их творчеством и поддерживали их. Треклист включает в себя такие песни, как «Southtown», «Breathe Babylon», «Rosa Linda», «Draw the Line», «Full Color» , и «Sabbath». Хотя сам альбом был спродюсирован людьми из «Atlantic Records», его сведение производилось в студии «Tooth & Nail Records».<br><br>

									Успех в мейнстриме (1999—2002)<br>
									Четвертый студийный альбом, записанный в 1999 году, назывался «The Fundamental Elements of Southtown». Он содержал в себе хиты «Southtown», а также «Rock the Party», на который был снят видеоклип. «Rock the Party» занял первое место среди клипов на одном из самых престижных американских шоу, представляющих музыкальные видео-творения. В то же время сингл «School of Hard Knocks» появляется в качестве саундтрека к фильму «Little Nicky» с Адамом Сэндлером (Adam Sandler) в главной роли. Все эти песни активно ротировали на радиостанциях и MTV, так что они быстро превратились в хиты. Альбом становится платиновым.<br><br>

									В 2001 году, в тот самый день и год, когда произошел теракт в Америке, и были разрушены башни-близнецы, а именно 11 сентября, «P.O.D.» выпускают один из лучших, если не самый лучший свой альбом за все время — «Satellite». Первый сингл с альбома — «Alive» стал самым популярным видео года на каналах MTV и MTV2. Эта популярность, а также позитивное послание, содержащееся в песне, моментально сделало сингл радио хитом.<br><br>

									Песня «Alive» считается именно тем прорывом в творчестве, который делает группу по-настоящему незыблемой в истории музыки. Так как сингл был выпущен именно в то время, когда для всего мира была особенно актуально тема терактов, и несправедливых убийств невинных жителей, она стала как бы гимном жизни, помогая людям переживать трудные времена.<br><br>

									Гитарист Маркос так говорил об этой песне в одном из интервью: «В мире так много бед, и в жизни почти каждого случаются несчастья. И если наша песня вдохновляет слушателя, и помогает ему справиться с проблемами, преодолеть свои негативные мысли, то мы выполнили свой долг, как артисты. Мы просто поем о том, что действительно важно услышать людям».<br><br>

									Второй сингл с альбома, «Youth of the Nation», был навеян ребятам из «P.O.D.» размышлениями о массовом насилии с применением оружия в американских школах, таких, как «Santana High School», «Columbine High School», и «Granite Hills High School». Песни 2002 года «Boom» и «Satellite», также стали очень популярны. В довершение всего, заключительный трек альбома «Portrait», получил множество наград и был представлен на Грэмми в номинации «Лучшее металл исполнение» в 2003 году. На этот раз альбом «Satellite» стал уже трижды платиновым.<br>
									Из группы уходит Marcos, запись альбома «Payable on Death» (2003—2006)<br>
									19 февраля 2003 года, гитарист Маркос Кьюриел покинул группу, чтобы заниматься своим проектом под названием «The Accident Experiment». По словам бывшего гитариста группы «Payable on Death», «его фактически выкинули из коллектива», — хотя официально причина была озвучена как «разногласия в группе на творческой почве». В любом случае, в своем новом проекте Кьюриел оставался на гитаре, вокалистом приняли Пита Стюарта (Pete Stewart), Тони Делохт (Tony Delocht) стал бас-гитаристом, Эрни Лонгория (Ernie Longoria) — барабанщиком (оба из «Sprung Monkey»). Первый альбом проекта «The Accident Experiment» был самостоятельно выпущен в 2003 году и назывался «Arena». В 2005 группа подписала контракт с «Rock Ridge Music», и их первый полноценный альбом «United We Fear» вышел 9 мая, 2006. На первый сингл «Sick Love Letter» даже успели снять видеоклип.<br><br>

									Сонни, Вав и Траа решили не распускать команду, а взять нового гитариста. На смену Маркосу пришел Джейсон Траби (Jason Truby) из христианской метал-группы «Living Sacrifice». Впервые он записал с ребятами саундтрек к фильму «The Matrix Reloaded» под названием «Sleeping Awake». В одном из интервью Сонни сказал, что Джейсон — главная причина того, что группа не распалась, и они именно поэтому до сих пор вместе.<br><br>

									4 ноября 2003 года «P.O.D.» выпускают свой четвертый студийный альбом с одноименным названием «Payable on Death». Он отличается наиболее темным образом, в нем явственно слышен напор на хард-рок звучание и мелодичность. После успеха таких синглов, как «Will You» и «Change the World», было продано 520 000 копий пластинки, и она получила статус Золотой.<br><br>
									«Testify» (2006)<br>
									Выпуск альбома «Testify» был запланирован на декабрь 2005, но релиз состоялся только 24 января следующего года. Зато в ноябре 2005 «P.O.D.» выпустили пластинку «The Warriors, Volume 2», которая включала в себя несколько демо-записей с грядущего альбома, чтобы поклонники группы смогли хотя бы составить представление о том, что их ожидает на «Testify».<br><br>

									Именно «Testify» стал последним альбомом с новым гитаристом Джейсоном Траби, который покинул группу в декабре 2006. Пластинку выпустили дополнительным эксклюзивным изданием «Testify, Limited Edition» с бонусами — комментариями группы о каждой песне на альбоме, четырьмя песнями и ремиксом. Видео на первый сингл с январского альбома, «Goodbye for Now», заняло первое место на шоу MTV — «TRL» и стало уже четвертым по счету видеоклипом, занимавшим первое место на «TRL» за всю историю группы. «Testify» разошелся тиражом 485.000 экземпляров по всему миру.<br><br>

									В альбоме можно заметить преимущественный уклон к хип-хопу и регги, который привнес Matisyahu, участвовавший в записи альбома. В поддержку новой пластинки ребята отправились в турне «Warriors Tour 2: Guilty by Association», которое началось в апреле. «P.O.D.» выступали с такими группами, как «Pillar», «The Chariot», «Maylene and Тhe Sons of Disaster».
									«P.O.D.» покидают «Atlantic Records», а Маркос возвращается в группу.<br>
									11 августа 2006 года «P.O.D.» официально объявили о расторжении контракта со своим звукозаписывающим лейблом «Atlantic Records». В газетах заявление прозвучало так: «Мы гордимся тем, что сотрудничали с такой компанией, как „Atlantic Records“. Но пришло время перемен. За все время нашей работы на лейбле, мы выпускали замечательный материал, который был нам дарован Богом, в соответствии с нашим мировоззрением, верой и любовью к музыке. В результате этого было распространено более 7 миллионов копий наших альбомов. Время снова мечтать о большем, создавать новые песни, и мы уходим с легким сердцем. P.O.D.».<br><br>

									В сентябре 2006 группа объявила, что они, с помощью «Rhino Records» выпускают альбом «Greatest Hits: The Atlantic Years», который вышел 21 ноября 2006. Они сняли видео на сингл «Going In Blind» — один из двух, включенных в их десятый по счету альбом. Они начали встречаться с представителями различных звукозаписывающих компаний с целью начать записывать свежий материал, который они надеялись выпустить к середине 2007 года.<br><br>

									На MySpace странице менеджером группы было официально подтверждено, что Траби покинул группу. «P.O.D.» утверждали, что в тот самый день, когда Джейсон принял решение уйти из группы, Маркос сам предложил вернуться и восполнить первоначальный, «классический» состав группы. Естественно, по словам самой группы, это произошло не без вмешательства высших сил.<br><br>

									Впервые после воссоединения Маркос с группой выступили на новогодней вечеринке 2006 в популярном шоу «Jimmy Kimmel Live!».<br><br>
									«Columbia Records», альбом «When Angels & Serpents Dance» (2007 — по сей день)<br>
									2 февраля 2007 года группа подписала контракт с «INO Records» — американской звукозаписывающей компанией в городе Нашвилле, штат Теннеси. Лейбл специализируется на современной христианской музыке. На ней записывались такие группы, как «Flyleaf», «Skillet», и «MercyMe». Лейбл иногда указывается как один из филиалов «Sony Records» и может быть упомянут в качестве «Epic Records» или «Columbia Records».<br><br>

									В июне 2007 года в «Rockbox», Сан-Диего, группа выступила с новой песней «Condescending» и «Addicted». Также они впервые открыли поклонникам название грядущего альбома — «When Angels & Serpents Dance».<br><br>

									В начале августа 2007, ребята стали участниками ежегодного христианского празднования «Harvest Crusade» на стадионе «Anaheim». Там они представили новую песню «I’ll Be Ready», первоначально названную «When Babylon Come For I» перед 42.000 зрителей. Это был рекорд посещаемости фестиваля за все три дня его проведения.<br><br>

									8 апреля 2008 свет увидел свежее творение группы — пластинку «When Angels & Serpents Dance». Это седьмой студийный альбом, на котором вновь появляется Маркос. Также в записи диска принимали участие Майк Муир (Mike Muir) из «Suicidal Tendencies», гитарист Пэйдж Гэмильтон (Page Hamilton) из «Helmet», церковный хор, а также «The Marley Sisters».<br><br>

									Альбом разошелся тиражом более 34 000 копий за первую неделю. Всего было продано 116 000 копий по всему миру. К сожалению, по сравнению с предыдущими продажами, этот альбом не принес большого коммерческого успеха. Но, несмотря на это, самое ценное ребята из P.O.D. уже завоевали — армию преданных поклонников по всему миру.<br><br>

									1 декабря на MySpace странице группы было размещено заявление о том, что скоро появится «секретный» сайт, в котором будет содержаться некоторая информация о предстоящем выпуске альбома «When Angels & Serpents Dance». На MySpace также был размещен отрывок из текста песни: «One must lead in the dance. Who’s leading you? Life is real when angels and serpents dance».<br><br>

									Ранним утром 10 декабря 2007 года было заявлено об обновлении официального сайта группы payableondeath.com. До этого там была представлена лишь фотография коллектива с Маркосом и некоторые музыкальные файлы прежних лет. С помощью почтовой рассылки группа сообщила множеству людей о том, что открытие сайта связано с наступлением нового 2008 года. 1 января сайт был открыт, и предлагал посетителям познакомиться с 30-секундным превью-видео на песню «When Angels & Serpents Dance». А уже 25 января 2008 года полная версия титульного трека стала доступна для скачивания широкой аудитории. 2 апреля альбом попал в интернет, и распространился чуть раньше, за 6 дней до официального релиза.<br><br>

									История духовного развития группы — это пример для каждого из нас, насколько важно быть терпеливым и настойчивым в достижении цели, оставаясь верному себе и своему мироощущению. Религия, или вера, как угодно, всегда была основным ключом к пониманию творчества группы, однако не правильно было бы рассматривать ее как всего лишь одну из команд мейнстрима; популярный и успешный коммерческий проект. Само существование группы «Payable On Death» и ее успех говорит нам о том, какое непоколебимое место в истории современного рока получила эта группа.


