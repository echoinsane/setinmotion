									<span style="font-size:34px;font-weight:bold;">I Killed The Prom Queen +</span><br><span style="font-size:25px;"> Black Tongue</span><br>
									Металкор группа из городка Аделаида, Австралия. Одна из первых австралийских металкор/хардкор групп,
									вышедших на мировую сцену. Группа образовалась в 2000 году. Первоначально группа называлась Child Left Burning.<br><br>
									
									В 2001 году после ухода первого вокалиста Люка Тэйлора ему на смену приходит Майкл Крафтер. <br><br>

									В 2002 году группа записывает первый 4х-песенный EP под названием Choose To Love Live Or Die. <br><br>

									В 2003 году Prom Queen вместе с земляками Parkway Drive записывают совместный сплит. 
									Для Parkway Drive это была первая запись. Позже в этом же году Prom Queen записывают свой 
									первый полноценный альбом под названием When Goodbye Means Forever. После его выхода группа
									 становится одним из лидеров австралийской металкор/хардкор сцены. В 2004 году первый альбом 
									 переиздается в США на лэйбле Eulogy Records. Позже в этом же году группа записывает
									 очередной EP под названием Your Past Comes Back To Haunt You, в который вошли 3 трека
									 из первой EP группы, а также 3 новые песни + акустическая шутливая песня Fast Food.
									 Запись стала культовой в Австралии. <br><br>

									В 2006 году группа расстается с Майклом Крафтером, который уходит в другую австралийскую группу - Carpathian.
									 На его место в группу приходит Эд Батчер. До Prom Queen Эд был вокалистом в британской группе The Hunt For Ida Wave. 
									Группа изменила звучание.Разница между двумя альбомами чувствительна. Будто совсем разные группы. <br><br>

									В 2006 году Prom Queen записывают альбом Music For The Recently Deceased. Альбом оправдал ожидания поклонников группы.
									Многие стали называть этот альбом одним из лучших металкор релизов в истории. Продюсировал пластинку Фредрик Нордстром,
									ранее работавший с такими монстрами как Dimmu Borgir, Darkest Hour, At the Gates и In Flames. Сразу после выхода альбома
									группа отправилась в тур по США с такими командами как Caliban, Evergreen Terrace, It Dies Today and Lamb Of God, а также
									Silverstein и Bleeding Through. <br><br>

									31 января 2007 Эд Батчер уехал в Британию в свой родной город и позже объявил о том, что он устал от пребывания в группе 
									и покидает ее. Тайрон из группы Mourning Tide временно выступает с Prom Queen в качестве основного вокалиста во время тура
									по Японии в поддержку последнего альбома. В апреле 2007 года группа начинает тур по Европе, в котором играют с All Shall Perish,
									Caliban, Bleeding Through. <br><br>

									7 апреля 2007 года группа объявляет о распаде, объясняя это решение невозможностью найти нового вокалиста. Гитарист группы Джона
									Вэйнхофен уходит в американскую металкор группу из Орендж Каунти - Bleeding Through <br><br>

									Во второй половине мая 2008 года компания «Stomp Records» переиздала альбом «Music For The Recently Deceased» лимитированным
									тиражом в 5000 копий. В течении этого месяца, музыканты так же совершили свой прощальный тур в компании с «Bring Me The Horizon»,
									«The Red Shore» и «The Ghost Inside», который был отснят для готовящегося к выпуску первого и последнего в видеографии группы DVD.
									Финальный концерт «I Killed The Prom Queen» состоялся 8 июня 2008 года в Брисбене, Австралия.<br><br>

									Однако, не смотря на то что группа в 2008 году распалась в этом же году музыканты решают записать новый альбом. Новый альбом 
									ожидается в середине 2012 года.<br><br>

