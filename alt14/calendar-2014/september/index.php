								<div id="desc_v_desc">
									<span style="font-size:40px;font-weight:bold;">Flyleaf</span><br>
									Идея создания команды «Flyleaf» принадлежит вокалистке Lacey Mosley (Sturm), 
									которая в начале нового тысячелетия прибыла в Тепл, штат Техас, с целью формирования 
									собственной группы, получившей название «Passerby».<br><br>

									Первым участником ее будущего коллектива стал барабанщик James Culpepper, а позднее
									 к ним присоединились гитаристы Jared Hartmann и Sameer Bhattacharya. Заключительным
									 звеном в составе группы стал басист Pat Seals, прибывший в 2002 году, после того как
									 покинул ряды своей прежней формации «The Grove». <br><br>

									В течении последующих двух лет, под вывеской «Passerby», музыканты выпустили три ЕР
									 и отыграли свыше сотни концертов на территории Техаса будучи под покровительством компании
									 «Runt Entertainment». В 2004 году группа попадает под взор президента «Octone Records»,
									 в следствии чего, уже в январе было объявлено, через официальный сайт компании, о том, что
									 группа становится частью семейства «Octone Records».<br><br>

									В марте, того же года, музыканты отправляются в Сиэтл, Вашингтон, для записи ЕР совместно
									 с знаменитым Rick Parashar (Pearl Jam, Blind Melon). В последствии, группа дает ряд концертов
									 в поддержку одноименного мини-альбома, которые проходят при участии именитых коллег:
									 «Breaking Benjamin», «Staind» и «3 Doors Down». В октябре 2004 года, пластинка появилась на прилавках музыкальных магазинов.<br><br>
									
									В 2005 году музыканты записывают свой первый лонгплэй, уже как «Flyleaf», при участии Howard Benson (Papa Roach, My Chemical Romance),
									 релиз которого состоялся 4 октября 2005 года.Почетными гостями альбома стали Dave Navarro (Jane’s Addiction) и Ryan White (Resident Hero).
									 Первым синглом с пластинки стала песня «I’m So Sick», вторым «Fully Alive» и композиция «All Around Me» стала третьим синглом.<br><br>

									В начале 2006 года, группа «Flyleaf» перебирается на лэйбл «J - Records», заключив договор между компаниями: «J-Records», «RCA» и «Octone Records».
									Обязанности дистрибъютора музыкальной продукции группы, среди христианских торговых сетей, взял на себя лэйбл «SRE Recordings». Участники коллектива - христиане.<br><br>

									10 ноября 2009 снова при участии Howard Benson выходит второй альбом «Memento Mori». На него были отобраны 14 лучших треков из 30 записанных к тому моменту.<br><br>

									22 октября 2012 года, за неделю до выхода «New Horizons», вокалистка группы Lacey объявила об уходе из группы, естественно ради семьи и воспитания сына.
									А среди фанатов и слушателей ходит мнение, что группа исчерпала себя, и уже начиная со второго альбома копирует сама себя, не придумывая ничего нового,
									и что приход новой вокалистки Kristen May уже не возродит группу.
