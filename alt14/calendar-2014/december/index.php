									<span style="font-size:40px;font-weight:bold;">Bring Me the Horizon</span><br>
									Bring Me the Horizon (BMTH) — английская металкор-группа из Шеффилда, Йоркшир, основанная в 2004 году пятью участниками других групп. Они исполняли музыку в стиле, который является смесью дэт-метала и металкора (также известный как дэткор) на своём первом альбоме. Но последний альбом имеет более эклектичный стиль, совмещая в себе элементы пост-хардкора, электроники, а так же металкора.<br><br>

									«Бринги» получили своё название, взяв за основу фразу из фильма «Пираты Карибского моря», которую произнёс капитан Джек Воробей: «Now… Bring me that horizon». После простой замены «that» на «the», Bring Me the Horizon стало официальным названием группы.<br><br>

									В настоящее время они подписали контракт с лейблом «Visible Noise Records», изначально же Bring Me the Horizon были подписаны на лейбл «Thirty Days of Night Records» и стали первой группой этого лейбла. Группа также имеет контракты с «Epitaph Records», США и «Shock Records», Австралия. <br><br>

									В прошлом выступали с такими группами, как Lostprophets, The Blackout, Killswitch Engage и The Haunted. В марте и апреле 2007 года BMTH совершили тур по Великобритании с теперь не существующей группой I Killed the Prom Queen. Они также играли на «Download Festival» 2007 с Iron Maiden, Slayer и другими.<br><br>

									Bring Me the Horizon записывали второй студийный альбом в начале 2008 года. В апреле и мае они были в Швеции, записывая альбом с Фредриком Нордстрёмом, который работал с такими группами как At the Gates, Arch Enemy, I Killed the Prom Queen и Dimmu Borgir. Альбом был выпущен 29 cентября 2008 года. «Suicide Season» полностью отличается от предыдущего альбома «Count Your Blessings», имевшего больше металкора, как в дебютном EP «This Is What the Edge of Your Seat Was Made For».<br><br>

									Группа недавно совершила свой первый тур по США в качестве хедлайнера, так же как и на «Warped Tour 2008». Bring Me the Horizon также выступали с Mindless Self Indulgence, In Case of Fire, Black Tide и Dir en Grey на «Kerrang!» и «Relentless» в 2009 году в туре по Великобритании. Во время тура по Северной Америке они выступят с Thursday, Cancer Bats, Four Year Strong и Pierce the Veil на «Taste of Chaos 2009».<br><br>

									Группа выступала на «Download Festival 2009». В марте 2009 года гитарист Кёртис Уорд покинул группу. Целый ряд причин его ухода озвучил басист Мэтт Кин, прокомментировав, что «это становилось очевидным — он не получал удовлетворения» и это было «просто как обязательство».<br><br>

									Джона Вайнхофен, прежде игравший в I Killed the Prom Queen и Bleeding Through, замещал Уорда на оставшихся выступлениях тура. Становился официальным участником Bring Me the Horizon. Покинул группу в январе 2013. <br><br>

