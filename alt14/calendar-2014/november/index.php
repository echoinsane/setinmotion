									<span style="font-size:40px;font-weight:bold;">Ennio Morricone</span><br>
									Эннио Морриконе родился 10 ноября 1928 года в семье профессионального джазового трубача Марио Морриконе и домохозяйки Либеры Ридольфи, он был старшим из 5 детей. Когда Эннио исполнилось 9 лет, он поступил в консерваторию Святой Сецилии в Риме, где проучился в общей сложности 11 лет, получив 3 диплома: по классу трубы в 1946 году, по классу оркестра (фанфар) в 1952 и по классу композиции в 1953.<br><br>

									Когда Эннио исполнилось 16 лет, он занял место второй трубы в ансамбле Альберто Фламини, в котором прежде играл его отец. Вместе с ансамблем Эннио подрабатывал, играя в ночных клубах и отелях Рима. А годом позже Морриконе устроился в театр, где проработал 1 год музыкантом, а потом 3 года композитором. В 1950 году он начал аранжировать песни популярных композиторов для радио. Обработкой музыки для радио и концертов он занимался до 1960 года, а в 1960 году Морриконе начал аранжировать музыку для телешоу.<br><br>

									Писать музыку к фильмам Эннио Морриконе начал лишь в 1961 году, когда ему было уже за 30. Он начал с итальянских вестернов, с жанра, с которым теперь прочно ассоциируется его фамилия. Широкая известность пришла к нему после работы над фильмами его бывшего одноклассника, режиссёра Серджио Леоне. Творческий союз режиссёра и композитора Леоне/Морриконе часто даже сравнивают с такими известными дуэтами, как Эйзенштейн/Прокофьев, Хичкок/Херрманн и Феллини/Рота. Позднее музыку Морриконе для своих фильмов пожелали заказать Бернардо Бертолуччи, Пьер Паоло Пазолини, Дарио Ардженто и многие другие.<br><br>

									С 1964 года Морриконе работал в звукозаписывающей компании «RCA», где аранжировал сотни песен для таких знаменитостей, как Джанни Моранди, Марио Ланца, Миранда Мартино и других.<br><br>

									Музыкальный вкус Эннио Морриконе очень сложно точно описать. Его аранжировки всегда были очень разнообразны, в них можно услышать и классику, и джаз, и итальянский фольклор, и авангард, и даже рок-н-ролл. Морриконе любит использовать разные инструменты, от гармошек и рожков до электрогитар, но его фирменный стиль — это использование женского голоса в качестве инструмента, оттеняемого сочной оркестровкой.<br><br>

									Вопреки расхожему мнению, Морриконе создавал не только саундтреки, он писал и камерную инструментальную музыку, с которой в 1985 году даже совершил тур по Европе, лично дирижируя оркестром на концертах.<br><br>

									Дважды за свою карьеру Эннио Морриконе сам снимался в фильмах, к которым он писал музыку, а в 1995 году про маэстро был снят целый документальный фильм. Эннио Морриконе женат, у него четверо детей, он живёт в Риме. Его сын Андреа Морриконе тоже пишет музыку для кино.<br><br>
									Биография Эннио Морриконе (Ennio Morricone)<br><br>

									Дата рождения: 10 ноября 1928 года<br>
									Место рождения: Рим, Италия<br><br>

									Эннио Морриконе родился 10 ноября 1928 года в Риме, Италия, в семье трубача. В 1938 году он начал заниматься в консерватории святой Цецилии по классу трубы, в 1944 году, прослушав курс гармонии, приступает к изучению композиции. <br><br>

									В октябре 1946 года Эннио Морриконе получил диплом трубача. Вскоре он, продолжая обучение в консерватории, начинает сочинять музыкальные произведения, делает аранжировки народных песен, начинает писать музыку для радио, телевидения и театра. <br><br>

									С середины 1950-х годов Морриконе начал сочинять и аранжировать музыку для кино, в титрах, правда, указывались имена более известных композиторов. Тогда же он работал для Джанни Моранди, а несколько позже занимался аранжировкой для Пола Анка. <br><br>

									В 1961 году на экраны вышла кинолента «Il Federale», которая считается первым фильмом, в работе над которым участвовал композитор Эннио Морриконе. Только за три года с 1961 по 1964 он написал музыку к 24 фильмам! Воистину, Морриконе олицетворяет целую музыкальную эпоху США. <br><br>

									В 1964 году началось его сотрудничество с кинорежиссерами Серджо Леоне и Бернардо Бертолуччи. Огромный успех фильма «За пригоршню долларов», поставленном его одноклассником Леоне, стал трамплином и для Эннио Морриконе (хотя композитор и скрылся под псевдонимом Лео Николс, а позже скрывался под псевдонимом Дан Савио). Знаменитый режиссер известных вестернов Сержо Леоне (Sergio Leone), несомненно сделал правильный выбор композитора к своим фильмам. Эннио стал не менее знаменит, чем Клинт Иствуд, снявшийся в главных ролях. <br><br>

									При создании звукового оформления фильма он использовал непривычные инструменты - электрогитары, колокольчики, панова флейта. Неудивительно, что в скором времени композитора стали приглашать многие известные режиссеры - за многие годы своего творчества он работал с Джилло Понтекорво, Мауро Болоньини, Пьером-Паоло Пазолини, Майком Николсом, Романом Поланским, Брайаном Де Пальма, Педро Альмодоваром. Не обошли вниманием Эннио Морриконе и такие мэтры режиссуры как Лючио Фульчи (Lucio Fulci) и Дарио Ардженто (Dario Argento) - лучшие режиссеры фильмов ужасов и триллеров!<br><br> 

									Всего Эннио Морриконе написал музыку почти к 400 фильмам. Среди них можно особо отметить такие картины, как «Хороший, плохой, злой», «Однажды на Диком Западе», «Однажды в Америке», «Миссия», «Неприкасаемые», «Птицы большие и малые», «Багси», «Аллонзафан», «Профессионал», «Новый кинотеатр ‘Парадизо’», «Волк», «Спрут». В 1995 году композитор был награжден «Золотым львом» Венецианского фестиваля за выдающие творческие достижения. <br><br>

									Также он сочинял много камерной музыки. Однако, именно музыка для фильмов сделала Морриконе знаменитым. Эннио Морриконе никогда не получал Оскара, но был очень близок к этому. Он был представлен в номинации «лучшая музыка к кинофильму» в таких фильмах как «Неприкасаемые»(1987), «Дни небес» (1978) и «Багси» (1991). В 2000 году получил «Золотой глобус» за фильм «Легенда о пианисте» (LEGEND OF 1900), а в 2001 получил его еще раз за фильм «Малена».А в 2001 году он наконец-то получает своего заслуженного Оскара за фильм «Малена».
