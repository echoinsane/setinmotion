												<span style="font-size:40px;font-weight:bold;">Enter Shikari</span><br>
												Enter Shikari (или стилизованно ΣΠΤΣR SHΦΚΔRΦ) — английская рок-группа, образованная в 2003 году, в месте под названием Сент-Олбанс, Англия. Они объединяют пост-хардкор с элементами различных электронных жанров, таких как электроника, дабстеп, транс и иногда драм-н-бэйс, создавая особенно уникальный звук. Их дебютный альбом, «Take to the Skies», был выпущен 19 марта 2007 и достиг четвёртой позиции в официальном британском хит-параде альбомов. Их второй альбом, «Common Dreads», был выпущен 15 июня 2009 и дебютировал в британском хит-параде альбомов с шестнадцатой строчки. Их третий студийный альбом, «A Flash Flood of Colour», был выпущен 16 января 2012 и держался наверху британского хит-парада альбомов в течение почти всей недели выпуска, однако в конечном счёте дебютировал с четвёртой строчки хит-парада. Группа завоевала большое количество поклонников благодаря своей страничке на сайте MySpace, где они публиковали все свои работы.<br><br>

