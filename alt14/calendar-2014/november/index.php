												<span style="font-size:40px;font-weight:bold;">Blue October</span><br>
												Blue October (с англ. — печальный октябрь) — американская группа из Хьюстона, штат Техас, играющая в стиле альтернативный рок. Группа создана в 1995 году и в настоящее время её коллектив составляют Джастин Фёрстенфелд (ведущий солист, ритм-гитара), Джереми Фёрстенфелд (барабаны, перкуссия), Райан Делахуси (скрипка, виолончель, мандолина, фортепиано, бэк-вокал) и Мэтт Новески (бас-гитара, бэк-вокал).<br><br>

												Создание группы<br><br>

												В 1995 году музыкант из Техаса Джастин Фёрстенфелд покинул созданную им же группу The Last Wish. Группа достигла локального успеха, выпустила два альбома и записывала третий. К тому моменту Джастин разделил процесс написания песен с другими участниками группы, что постепенно разочаровало его. Он захотел начать все с чистого листа и ушел из группы, забрав с собой 4 песни, которые были очень важны для него. Вскоре он вместе со своим одноклассником, скрипачом Райаном Делахуси, основывают «Blue October». Когда за неделю до первого выступления группы у них еще не было барабанщика, Джастину пришлось пригласить своего брата — Джереми, начинающего барабанщика. С тех пор место перед ударными оставалось именно за ним. Басистку Лиз Маллалай Райан встретил в ресторане Auntie Pasto, где в тот момент работал. Однажды вечером она решила там поужинать и между ними завязался разговор, который быстро превратился в предложение присоединиться к группе. На тот момент она стала самой старшей участницей группы. Свободная комната в её просторном доме в Хьюстоне стала первым репетиционным местом для «Blue October». Джастин и Райан планировали переехать в Остин, однако, привыкший к сельской жизни Джереми убедил их остаться в Сан Маркосе. Джастин Фёрстенфелд приступил к написанию песен. Он и Райан Делахуси иногда находили время между работой для выступлений дуэтом. Все трое постоянно ездили из Сан Маркоса в Хьюстон для репетиций с Лиз, изредка давая там концерты.<br><br>

												Происхождение названия<br><br>

												Основной версией происхождения названия является кратковременное пребывание Джастина в госпитале, которое случилось однажды в октябре. В одном из интервью Джастин вспоминает школьные годы, когда ему пришлось временно лечь в больницу, так как у него были некоторые психические проблемы. Когда он вышел из больницы, то понял, что он должен сделать что-то позитивное с этой депрессией. И он начал писать песни.<br><br>

												Еще один рассказ говорит о том, что в тот период Джастин очень устал от отношений, которые были тогда. Очень устал от наркотиков, которые тогда принимал. Примерно похожие ситуации были и у других участников группы. Они поняли, что пришло время что-то менять. Оздоровить свою жизнь и вырасти. Это был месяц самореализации и внутреннего роста. Когда они начали обсуждать название, кто-то предложил «Blue October» и остальные посчитали, что это название отлично подходит. Позже, название наложило отпечаток и на музыку, которую играет группа.<br><br>

												В другом интервью Джастин рассказал, что в тот период они активно употребляли наркотики, потому что думали, что это и есть музыка. Его кумирами тогда были Курт Кобейн и Эллиотт Смит, один из которых закончил свой путь самоубийством и оба сидели на наркотиках. Джастин сходил с ума от наркотиков в 20 лет, а в октябре того года понял, что должен очиститься и начать делать что-то положительное. Октябрь 1995 года был для него ужасным месяцем. Это был один из тех месяцев, когда понимаешь, что пора собраться, изменить всё или у тебя уже ничего не получится. Это был период душевных поисков, выражения их в поэзии и вдохновения. Он выбрал октябрь месяцем для внутреннего роста и поэтому назвал группу «Blue October». Это был самый печальный месяц в его жизни, но он стал отправной точкой. С тех пор его жизнь круто изменилась.<br><br>

												Когда о названии спросили Мэтта, он ответил, что в тот месяц, когда образовалась группа, произошло что-то очень неприятное и «blue» (синий) был цветом, в который была окрашена картина того месяца. По иронии, октябрь стал месяцем, в который всегда происходит множество различных событий с группой.<br><br>

												The Answers<br><br>

												Осенью 1997 года начался процесс записи дебютного альбома, который получил название The Answers. Запись проходила в студии Sound Art. Продюсером выступил Брайан Бейкер. Богато текстурированный и безупречно записанный, альбом представляет собой часовую любовную историю. Для достижения такого результата на дебютной пластинке группе пришлось заручиться материальной поддержкой родителей братьев Фёрстенфелдов. Их отец, Дэн, являлся менеджером группы и устраивал выступления «Blue October» в Далласе и других частях Техаса.<br><br>

												Альбом получился очень грустный и, по словам вокалиста Джастина Фёрстенфелда, слушать его по этой причине ему самому не хочется.<br><br>

												Это был хороший дебют: было продано 6 тысяч копий только в Хьюстоне. Благодаря связям через фанов с локальной телевизионной сетью ABC, «Blue October» удалось назначить выступление на утренних новостях. Это было первое выступление группы на телевидении, в прямом эфире.<br><br>

												Consent to Treatment<br><br>

												«Blue October» обратили на себя внимание ведущих записывающих лейблов. В 1999 году они подписали контракт с Universal Records на выпуск их второго альбома Consent To Treatment.<br><br>

												В это время группу покинула бас-гитаристка Лиз Маллалай и ей на смену пришел Мэтт Новески, который быстро вписался в коллектив. Он стал соавтором песни James, а также создал обложку для альбома. Кроме того, в январе 2000 года в состав добавился пятый участник — гитарист Брэнт Коултер. Джастин Фёрстенфелд решил избавиться от гитары, чтобы стать ближе к слушателям во время концертов. По этой причине и был приглашен Коултер. Ему пришлось выучить уже существующие песни, к которым вскоре Джастин добавил десяток новых, в создании которых Коултер уже принимал участие. Однако в октябре 2000 года он покинул группу.<br><br>

												Говоря перед релизом об альбоме, Джастин Фёрстенфелд признавался:<br>
												«Для меня не важны миллионные продажи, достаточно возможности просто создавать музыку. Но если альбом достигнет успеха, я буду счастлив. Я не очень уверен, готов ли мир к выходу альбома, готов ли Хьюстон, готовы ли мои родители, однако сам я готов, а остальное меня уже не волнует.»<br><br>

												Universal Records ожидали от «Blue October» оптимистичного альбома для любителей нью-метала. Кроме того, лейбл настаивал на том, чтобы «Blue October» избавились от скрипки. И, разумеется, ожидались большие продажи альбома. Группа отстояла наличие скрипки в своем арсенале, что не очень понравилось лейблу. Альбом не оправдал возложенных ожиданий, поскольку не достиг запланированного успеха. Песни группы отказывались ставить в ротацию на радио, даже в их родном городе. В итоге, по цифрам 2005 года, было продано около 70 тысяч копий пластинки. По этой и некоторым другим причинам Universal Records перестали сотрудничать c «Blue October» в середине 2001 года. Иронично, третий альбом группы, History For Sale является отзывом о контроле, который лейбл поставил на группу в процессе записи Consent to Treatment. Джастин Фёрстенфелд решил на какое-то время заняться сольным проектом 5591.<br><br>

												History For Sale<br><br>

												После расставания с лейблом в группе произошли изменения. В 2001 к группе присоединился новый гитарист Си Би Хадсон, заменивший Брэнта Коултера. В октябре 2002 года по личным причинам группу покинул Мэтт Новески, которого заменил Дуэйн Кэйси, представленный как новый участник группы 25 декабря. На двоих они сыграли партии бас-гитары на альбоме History For Sale. Дуэйн Кэйси после записи альбома также покинул группу в августе 2003 года. Бас-гитара перешла к Пайпер Ски. Она оставила след в истории «Blue October» на съемках концертного альбома Argue with a Tree… Однако и она надолго не задержалась, покинув группу в конце 2004 года. После чего вернулся Мэтт Новески, который с тех пор является неизменным участником «Blue October».<br><br>

												Собственными силами группа выпустила альбом History For Sale на лейбле Brando Records. Как написал Джастин Фёрстенфелд в биографии группы на официальном сайте, новый альбом стал своеобразной формой мести их старому лейблу. Альбомом History For Sale «Blue October» хотели показать, что они занимаются музыкой не для создания радио-хитов, а для того, чтобы приблизиться к своим слушателям.<br><br>

												Альбом стал неожиданно популярным, продажи намного превысили ожидания, вследствие чего Universal вновь предложили группе сотрудничество. «Blue October» с радостью согласились, о чем свидетельствует обновленная биография на сайте: «Мы рады возможности поработать вновь. Мы обнялись, поцеловались, загладили вину и теперь у нас лучший секс в жизни». Вскоре, Universal переиздали History For Sale.<br><br>

												«Blue October» завоевали ещё больше поклонников, когда их сингл Calling You, появившийся в саундтреке к фильму The American Wedding, стали крутить на радио в Далласе. Группу пригласили участвовать на большом ежегодном концерте, также известном под названием «Edgefest». Позже, песни Inner Glow и Razorblade стали хитами. Тем не менее, сингл Calling You оставался наибольшим мейнстрим успехом группы вплоть до 2006 года. В 2004 году группа выпустила концертный CD и DVD с записью двухчасового выступления в Далласе, состоявшемся 4 июня 2004 года. Диск получил название Argue with a Tree… в честь строчки из песни Weight Of The World, которая впоследствии была включена в альбом Approaching Normal.<br><br>

												Foiled<br><br>

												В начале 2005 года Джастин Фёрстенфелд переехал в Лос-Анджелес, где начал работу над демо песен для нового альбома. Большинство выбранных песен являлись старыми песнями «Blue October» и его сольного проекта 5591. Однако были и новые композиции, которым, в итоге, и было суждено стать главными хитами группы. Альбом был записан летом 2005 года. Релиз несколько раз откладывался и был перенесен на следующий, 2006 год.<br><br>

												В 2005 году в группу вернулся Мэтт Новески. Он принял участие в записи альбома и последовавшим осенью концертным туром. Первое выступление после возвращения состоялось 18 сентября. Кроме того, данный концерт памятен тем, что тогда состоялась презентация песни Hate Me. Также, в сетлист группы была возвращена песня You Make Me Smile, которую Мэтт написал совместно с Джастином во время первого периода пребывания Мэтта в составе «Blue October».<br><br>

												В январе 2006 года группа отыграла несколько акустических мини-выступлений на различных радиостанциях США, во время которых «Blue October» активно продвигали песню Hate Me. Очень быстро сингл стал самым часто заказываемым треком на американском радио. Песня добралась в чартe «Modern Rock» журнала Billboard до второго места.<br><br>

												Релиз альбома Foiled состоялся 4 апреля 2006 года. «Blue October» отправились в турне в его поддержку. Однако, не все пошло гладко. 29 апреля, после выступления в городе Клирвотер, штат Флорида, Джастин Фёрстенфелд упал и сломал ногу. Спустя три дня, во время следующего концерта ему пришлось петь сидя в инвалидной коляске. Роль гитариста же перешла Джулиану Мандрэйку. Группе пришлось отменить все выступления до июня. Джулиан выступал вместе с группой вплоть до осени, до момента, когда Джастин Фёрстенфелд выздоровел. Однако, перед концертом 6 ноября Джастин вновь травмировал ногу и группе снова потребовалась помощь Джулиана Мандрэйка. Вместе с ним «Blue October» выступили 14 ноября перед Rolling Stones в Бойсе, штат Айдахо. Последнее выступление Джулиана в составе «Blue October» состоялось 30 ноября в Далласе.<br><br>

												Вторым синглом с альбома стала песня Into the Ocean, появившаяся на радио 17 июля 2006 года. Клип к песне дебютировал на 13 месте на «VSpot Top 20 Countdown» канала VH1 и добрался до 1 места в середине февраля 2007 года.<br><br>

												Завоевав новых поклонников альбомом Foiled, группа расширила свой тур. Первоначально ограниченные Техасом, средним западом и юго-западом США, «Blue October» сейчас устраивает туры по главным точкам, покрывающим все штаты, также по Гаваям, Мексике, Англии, Германии и Нидерландам.<br><br>

												Последний сингл с четвёртого альбома, She’s My Ride Home, группа сыграла на «Late Night with Conan O’Brien» 25 апреля 2007 года.<br><br>

												25 сентября 2007 года группа выпустила переиздание четвёртого альбома — Foiled for the Last Time. Издание включало в себя два диска — обновлённый «Foiled» и концертную запись, сделанную 24 марта 2007 года в родном для группы городе Остине.<br><br>

												В августе 2008 года Джастин Фёрстенфелд принял участие в небольшом туре писательницы Стефани Майер по четырём городам Америки. Тур был посвящён выходу четвёртой книги из серии «Сумерки». Сама писательница призналась, что черпала вдохновение в музыке «Blue October». Во время тура Джастин исполнил две песни, которые затем вошли в пятый альбом Approaching Normal. Одна из них, My Never, у поклонников серии теперь прочно ассоциируется с безнадёжным чувством Джейкоба к Бэлле.<br><br>

												Approaching Normal<br><br>

												Работа над альбомом Approaching Normal началась в начале 2008 года. В качестве продюсера выступил Стив Лиллиуайт, известный по своей работе с Питером Гэбриэлом, The Smiths, U2 и Travis. Запись альбома была завершена 24 октября 2008 года, а 8 января следующего года материал нового альбома был полностью сведён.<br><br>

												Первый сингл Dirt Room начал активно ротироваться на американских радиостанциях в начале декабря 2008, а 23 декабря состоялся его цифровой релиз. Клип на эту песню вышел 27 января. Синглу удалось подняться лишь на 12 строчку в «Hot Modern Rock Tracks» журнала Billboard. 7 января «Blue October» сделали подарок, выложив для свободного скачивания бонус-трек к будущему альбому, песню Graceful Dancing. Довольно старая песня, ранее исполнялась во время сольных концертов Джастина Фёрстенфелда под псевдонимом 5591. 10 февраля песня Kangaroo Cry появилась в на официальном саундтреке к сериалу «NCIS».<br><br>

												Долгожданный релиз пятой студийной пластинки Approaching Normal состоялся 24 марта 2009 года. Спустя месяц, 24 апреля, вышел второй сингл с альбома — Say It. В течение 2009 года «Blue October» совершили два гастрольных тура по США и дважды посещали Европу с концертами. Второй гастрольный тур по Штатам был посвящён помощи людям, имеющим психологические проблемы и склонным к самоубийству, он получил название «Pick Up The Phone Tour». Вокалист группы, Джастин Фёрстенфелд, даже выступил с обсуждением данной проблемы перед Конгрессом США. Однако 22 октября поступила неожиданная новость — Джастин был госпитализирован с сильными психологическими проблемами. Не секрет, что с юных лет он страдает от этого. Вскоре он был выписан, однако, гастрольный тур группа была вынуждена отменить. До окончания года было сыграно всего 3 концерта. Чтобы не расстраивать поклонников, «Blue October» сделали им подарок на День Благодарения, отмечавшийся 26 ноября. В своём официальном интернет-магазине группа сделала бесплатную раздачу четырёх бонус-треков с последнего альбома, ранее доступных лишь обладателям версии, выпущенной на виниле.<br><br>

												Шестой альбом<br><br>

												Весной 2010 года Джастин Фёрстенфелд неоднократно в интервью заявлял, что материал для шестого по счету студийного альбома группы уже готов и скоро группа приступит к записи. 1 июня в микроблоге появилось сообщение, что группа приступила к подготовительной работе над альбомом. Продюсером станет Тим Палмер, работавший с такими музыкантами как David Bowie, Robert Plant, The Cure, U2, HIM, Ozzy Osbourne, Duran Duran и многими другими. Запись проходила в «Matchbox Studios» в городе Остин.<br><br>

												В конце июля группа дала 5 акустических выступлений в городах Остин, Даллас и Хьюстон (штат Техас). Одно из них, концерт в Остине 22 июля, было записано для последующего релиза. Концертный альбом получил название Ugly Side: An Acoustic Evening With Blue October, его релиз состоялся 10 мая 2011 года.<br><br>

												В октябре 2010 года закончилось почти двухлетнее турне в поддержку альбома Approaching Normal, а спустя месяц группа вернулась в студию. Согласно сообщению в микроблоге, по состоянию на 8 декабря было записано уже 10 песен. Одну из них Джастин Фёрстенфелд исполнил во время акустического выступления 2 декабря, предположительное название «Honesty».<br><br>

												Вечером 14 декабря на официальном форуме было опубликовано сообщение менеджера группы Майка Свинфорда, сообщающее о том, что в поисках новых вызовов группу покинул гитарист Си Би Хадсон. Кроме того, Чарльз решил проводить больше времени со своей семьей. «Blue October» объявили поиск нового гитариста. На время поисков к группе вновь присоединился Джулиан Мандрэйк который в 2006 году выступал с группой после травмы Джастина Фёрстенфелда.<br><br>

												В феврале 2011 г. состоялась премьера фрагментов нескольких песен с будущего альбома. 8 февраля участник групп «Longwave» и «Hurricane Bells» Стив Шильтц, принявший участие в записи нового альбома «Blue October» выложил видео, где они совместно в студии работают над песней Feel Again. Затем, 17 и 18 февраля Джастин Фёрстенфелд в качестве приглашенного ведущего на радиостанцию «101X» презентовал фрагменты еще четырех песен: «The Chills», «The Flight: Lincoln to Minneapolis», «The Waiting» и «The Getting Over It Part».<br><br>

												3 апреля 2011 Джастин Фёрстенфелд анонсировал, что новый альбом будет выпущен на лейбле, основанном самой группой - Uptown Records, а продвижением будет заниматься компания «RED Distribution», подразделение Sony для независимых исполнителей. Релиз шестого студийного альбома назначен на август-сентябрь 2011 г. Рабочее название альбома - «Any Man In America»
