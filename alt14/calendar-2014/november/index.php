									<span style="font-size:40px;font-weight:bold;">Limp Bizkit</span><br>
									История группы началась в 1994 году со знакомства вокалиста Фреда Дёрста и басиста Сэма Риверса, который привёл в группу своего кузена, джазового барабанщика Джона Отто. Затем к ним присоединился гитарист Роб Уотерс, который, впрочем, вскоре покинул Limp Bizkit. Сменив Уотерса, в группу пришёл гитарист Уэс Борланд. В 1996 году к группе присоединяется DJ Lethal, бывший участник знаменитой хип-хоп группы House of Pain.<br><br>
									В 1997 году Limp Bizkit выпустили дебютный альбом «Three Dollar Bill, Yall$», оказавшийся очень успешным и проданный тиражом более миллиона экземпляров. В следующем году группа развивает этот успех, гастролируя по США вместе с такими группами, как KoЯn, Orgy, Rammstein и Incubus.<br><br>
									Настоящий успех пришёл в 1999 году, когда группа выпустила альбом «Significant Other», ставший впоследствии мультиплатиновым, а затем и в 2000 году, с выходом их самого успешного альбома «Chocolate Starfish and the Hot Dog Flavored Water», ставшего платиновым только за первую неделю продаж и получившего премию MTV как лучший альбом года. Всё это возвело Limp Bizkit в ранг мировых звёзд первой величины.<br><br>
									В 2001 году из группы ушёл гитарист Уэс Борланд, что стало очень серьёзной потерей для коллектива. Замену Борланду нашли только через два года бесконечных проб и конкурсов — ей стал бывший гитарист группы Snot Майк Смит. С ним группа записала альбом «Results May Vary», уже не вызвавший такого ажиотажа, как два предыдущих.
									В 2004 году, сменяя Майка Смита, в группу неожиданно вернулся Уэс Борланд. Вернувшись к своему классическому составу, в 2005 группа записала EP-альбом «The Unquestionable Truth (Part 1)». Limp Bizkit принципиально отказались сопровождать выход этого альбома какими-либо видами промоушена, рекламы или интервью.
									В конце 2005 года по настоянию рекорд-лейбла вышел сборник хитов «Greatest Hitz».<br><br>
									В 2006 году группу вновь покинул Уэс Борланд в последствии в 2007 году выпустивший первый альбом своего собственного проекта Black Light Burns.
									12 февраля 2009 года оригинальный состав, включающий в себя Фреда Дёрста, Уэса Борланда, Сэма Риверса, Джона Отто и Dj Lethal возвращаются после 8-летнего перерыва, чтобы вновь погрузить нас в мир своей музыки. Фред Дёрст и Уэс Борланд сделали следующее заявление:<br><br>
									«Мы решили, что нас гораздо больше бесит современная тяжёлая музыка, чем мы бесим друг друга. Вне зависимости от того, насколько разошлись наши пути, мы понимаем насколько мощная и уникальная энергия идёт от этой группы из пяти человек - такая энергия, которую не найти больше нигде. Поэтому Limp Bizkit вернулись.» Летом 2011 года Limp Bizkit выпуститят свой новый альбом. По словам Фреда Дёрста, альбом не будет похож на прошлые. Название ему - «Gold Cobra».<br><br>

