									<span style="font-size:40px;font-weight:bold;">Apollo Brown & Guilty Simpson</span><br>
									Apollo Brown отличается от большинства продюсеров. Он вырос на музыке поп-рок группы «Seals and Crofts», известной своим хитом «Summer Breeze».<br><br>

									Родился Apollo Brown в рабочей семье в городке Гранд-Рапидс, штат Мичиган, и его ранние музыкальные влияния были далеки от того уличного жанра, который стал вдохновением для многих современных хип-хоп исполнителей.<br><br>

									Но, это не значит, что с приходом рэп музыки в жизнь Apollo, все сразу перевернулось с ног на голову. Вовсе нет!<br><br>

									29-ти летний битмейкер вспоминает, что с начала 90-х он просто увлекся творчеством таких рэп исполнителей, как Gang Starr, Wu-Tang, Mobb Deep, M.O.P., Nas и Black Moon.<br><br>

									А в 1996 году битмейкер самоучка уже начал пробовать сочинять свою собственную музыку при помощи аудио программы «Voyetra», а потом перешел на более продвинутую «Cool Edit 2000», которой пользовался в течение последующих восьми лет.<br><br>

									После окончания Университета штата Мичиган по специальности «управление бизнесом», Apollo Brown переехал в западную часть Детройта и примкнул к местному хип-хоп сообществу, где он образовал рэп группу «Black Day» вместе со своим одноклассником и представителем клана Wu-Tang - рэпером Bronze Nazareth.<br><br>

									Вскоре группа распалась, потому что каждый из участников решил заняться сольной деятельностью.<br><br>

									Apollo переехал в восточную часть Motor City, где продолжил работать, прежде чем разочаровался в музыкальном бизнесе и отошел от продюсерской деятельности на целых два года.<br><br>

									Это случилось в середине 2000-х годов, и новоявленный продюсер не видел будущего в своей работе, которой он посвятил свою жизнь, потому что хип-хоп приобретал статус попсовой музыки, превратившись в конвейер шаблонов для широких масс.<br><br>

									Но без творчества жизнь Apollo Brown стала совсем серой и безвкусной, и он вновь вернулся к своему ремеслу, выпустив инструментальные альбомы «Skilled Trade» (2007) и «Make Do» (2009).<br><br>

									Популярность битмейкера росла и его стали приглашать к сотрудничеству многие исполнители, например такие, как Detroiters Finale, MaGestiK LeGenD, Danny Brown, Paradime и Supastition.<br><br>

									В 2009 году он выиграл в чемпионате «Detroit Red Bull Big Tune Championships» и участвовал в его международном финале.<br><br>

									А в декабре 2009 года он был подписан в качестве продюсера на «Mello Music Group».<br><br>
									До этого Apollo уехал из Мичинана в 2007 году, чтобы поступить на должность имущественного инспектора в Кливленде, штат Огайо. Спустя неделю после подписания контракта, он был уволен с этой работы.<br><br>

									Apollo считает, что это увольнение стало неким благословением и своеобразным толчком для возвращения в музыкальный бизнес.<br><br>

									В 2010 году он выпустил замечательный альбом «The Reset» в котором приняли гостевое участие рэпер Big Pooh из группы Little Brother, Black Milk, MED, Grap Luva, labelmates Kenn Starr, Oddisee и Diamond District.<br><br>

									В этом же году битмейкер принял участие сразу в нескольких проектах: в альбоме «Brown Study», в составе группы UPS вместе с Boog Brown, в новом альбоме индии рэпера Daily Bread и в работе нью-йоркского эмси Hassaan Mackey.<br><br>

									Еще одним ярким событием этого года стал релиз долгожданной LP «Gas Mask» проекта The Left, где Apollo Brown работал вместе с DJ SoKo и MC Journalist 103.<br><br>

									Apollo Brown негативно относится к коммерческой музыке, и твердо уверен, что андеграунд исполнители являются лучшими рэперами. За свою творческую карьеру битмейкер создал много хорошей музыки, много повидал, многим был впечатлен.<br><br>

									Он сконцентрирован на создании музыки в стиле его любимой эпохи 1991-96 годов. Он не придерживается, какого то определенного стиля и творит все, что ему придет в голову. Apollo очень застенчивый человек и музыка является его способом самовыражения.<br><br>
