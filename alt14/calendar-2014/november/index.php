									Образование группы Limp Bizkit произошло в 1994 году в Джексонвилле (штат Флорида). Музыка команды – это смесь металла и хип-хопа, которая позже начала называться «ню-металл». В группу вошли вокалист Фред Дерст, басист Сэм Риверс, гитарист Вэс Борланд и барабанщик Джон Отто. В 1995 году Фред Дерст познакомился с группой Korn, которой понравилась демо-запись молодых музыкантов. Таким образом, о них узнал продюсер Korn Росс Робинсон. В следующем году Limp Bizkit совершают совместное турне с группами Deftones и House Of Pain. Несколько позже в Limp Bizkit появляется DJ Lethal, так как его проект House Of Pain завершил свое существование. Группа заключает контракт с Flip/Interscope и в 1997 году выпускает дебютный альбом под названием «Three Dollar Bill, Y’all». Поклонники группы раскупили 1,5 миллиона экземпляров, чему способствовали агрессивные и яркие выступления Limp Bizkit и их присутствие на фестивале OzzFest.<br><br>

									Популярность группы растет, благодаря записи Фреда Дерста на альбомах Soulfly и Korn. В 1999 году выходит второй диск Significant Other, который был записан вместе с DJ Premier и рэппером Методом Мэном. Альбом занимает 1 место в чартах США, а сама группа становится главной альтернативной командой в стране. Летом 1999 года Limp Bizkit дают концерт в рамках фестиваля Woodstock, во время которого поклонники учиняют настоящее погром. Происшествие не остается незамеченным со стороны прессы, которая обвинила в провокации инцидента лидера группы.<br><br>

									2000 год был отмечен выходом боевика Take A Look Around, положенным в основу фильма «Миссия невыполнима 2» . Альбом Chocolate Starfish And The Hot Dog Flavored Water выходит в лидеры американских чартов, а песня Rollin’ становится популярной в Европе. Она же в январе 2001 года две недели является лидером хит-парада Великобритании. В конце сентября Вэс Борланд вместе с Фредом участвует в благотворительном концерте America: Tribute To Heroes, посвященном пострадавшим в Вашингтоне и Нью-Йорке 11 сентября, а в октябре Вэс, записав диск для своего проекта Big Dumb Face, покидает группу. Весь 2002 год Limp Bizkit безуспешно прослушивают гитаристов на его место и даже просят Вэса вернуться, но вместо этого он начинает подбор вокалистов для своего проекта под названием Big Dumb Face, названного в будущем Eat The Day. Только в апреле 2003 года место Вэса в Limp Bizkit занял Майк Смит.<br><br>

									С обновленным составом группа принялась активно работать над новым альбомом. Однако оба новых альбома музыканты Limp Bizkit забраковали, и они были отправлены в архив группы. В прессе долгое время сообщалось то о переносе даты выпуска нового альбома, то о его предполагаемых названиях. В итоге альбом был собран из разных, ранее записанных, песен и вышел под названием Results May Vary.<br><br>

									Новый диск был разобран по кусочкам критиками, тем не менее, после дебютного исполнения он стал лидером в горячей сотне журнала «Биллборд» и получил платиновый статус в Штатах. Однако успеха прежних работ Limp Bizkit он не достиг. Кавер-версия композиции The Who – Behind Blue Eyes, выпущенная в виде песни, стала очень популярной на радио и, в то же время, многие интернет-издания признали ее худшей в сравнении со всеми ранее существующими кавер-версиями. Летом 2004 года в группу вернулся Вэс Борланд и сразу подключился к работе над новым материалом, о чем Дерст официально сообщил СМИ в августе, сказав, что Limp Bizkit неплохо поработали с Майком Смитом, однако он все равно не очень подходил для них. Группа пригласила знаменитого продюсера Росса Рубинсона, с которым был записан их дебютный альбом под названием Three Dollar Bill Y’all, и в полном составе занялась записью нового альбома.<br><br>

									Май 2005 года был ознаменован выходом новой работы Limp Bizkit. Время звучания этого мини-альбома под названием The Unquestionable Truth Pt.1 всего 30 минут. Перед выходом альбома не было ни масштабной рекламной кампании, ни промоушена, поэтому его очень медленно раскупали. Однако именно такова была стратегия группы, они сами добились, чтобы шумихи и рекламы вокруг альбома не было. Звучание The Unquestionable Truth оказалось более жестким и агрессивным по сравнению с предыдущими работами. В альбоме Дерст в первый раз запел о тех вещах, которые волнуют его в окружающем мире. В ноябре 2005 года появился сборник лучших композиций группы, который назвали Greatest Hitz. Выход этого диска был очень широко освещен в СМИ. Дерст в завершении рекламной кампании рассказал, что вынужден был участвовать в промоушене сборника, который он даже выпускать не хотел. Параллельно с диском вышел и DVD. На нем были записаны все клипы Limp Bizkit.<br><br>

									В начале 2006 года были высказаны предположения, что в течение года возможно появление целых двух новых релизов Limp Bizkit – продолжение (2-я часть) The Unquestionable Truth, а также очередной полнометражный альбом. Но сами участники группы долго не давали комментариев по этому поводу, чем внесли беспокойство в ряды своих фанатов. Тогда Фред Дерст согласился на интервью, однако в нем сообщил, что в данный момент снимает свой первый фильм. Ни одного слова о предстоящем выходе новых альбомов Limp Bizkit в его интервью не прозвучало. Кроме того, собственными проектами занялись также басист Сэм Риверс (проект The Embraced) и Вэс Борланд.<br><br>

									На сегодняшний день группа снова работает в оригинальном составе. Они проводят масштабные гастроли, а также готовятся к выпуску нового диска.<br><br>

									В России Limp Bizkit впервые выступила с концертом 27 мая 2009 года на арене Ледового Дворца в Санкт-Петербурге, далее 29 числа того же месяца они дали концерт в Москве на сцене Зеленого театра ЦПКиО им. Горького. Московский концерт прошел не совсем обычно, так как одну из песен группа исполнила вместе с отечественным рэпером Серёгой. Данное обстоятельство вызвало негодование у фанатов Limp Bizkit. Следующим пунктом гастролей стал Киев, где группа выступила 31 мая во Дворце спорта.

